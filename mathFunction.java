package Core_Java;

public class mathFunction {

	public static void main(String args[]) {
		//Math function or method it's default function to execute the math function. no need to create the object for math function
		System.out.println(Math.max(10,50));  // to find the maximum number
		System.out.println(Math.min(26,50)); //to find the minimum number
		System.out.println(Math.ceil(4898.1)); // to round up the value / near to the round up value
		System.out.println(Math.abs(-987)); // to convert the negative value to  value
		System.out.println(Math.sqrt(81));  // to get the square route value
		System.out.println(Math.random());//to generate the random number - value should be double
		System.out.println((int)(Math.random()*101)); //to get random number as a whole number so we typecasting into Int datatype
	   
	} 

}
